# Rechnen

# Grundrechenarten + - * /
print(4 + 3)
print(25 - 4)
print(84 * 2)
print(7 / 7)

# Potenz **
print(2 ** 10)

# Rechnen mit Variablen
zahl1 = 6
zahl2 = 7
